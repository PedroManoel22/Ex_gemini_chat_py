# Crie uma lista de strings onde cada string seja o nome de uma fruta em maiusculas da lista frutas = ['maçã', 'banana', 'uva'].

frutas = ['maçã', 'banana', 'uva']
frutas_maiusculas = [fruta.upper() for fruta in frutas]
print('Lista de entrada:', frutas)
print('Lista com frutas maiúsculas:', frutas_maiusculas)